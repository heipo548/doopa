// 同じ凡例を参照できるようにするため（描画は sprites.ts が担当）。
export const CHAR_TO_TILE: Record<string, number> = {
  ".": 0, // 草
  ",": 1, // 草（白い小花入り）
  "-": 2, // 道
  "~": 3, // 水
  "*": 4, // 花
  T: 5, // 木
  f: 6, // 柵
  r: 7, // 岩
  W: 8, // 家のかべ
  R: 9, // 屋根
  D: 10, // とびら
  S: 11, // たて看板
  B: 12, // けいじばん
  F: 13, // はたけ
  "=": 14, // 橋
  P: 15, // ほしみだいの床
  s: 16, // 階段
  k: 17, // 闇（マップ外）
  K: 18, // 明るい草（誕生地点）
  b: 19, // しげみ
};
export const TILE_COUNT = 20;
